package golang

import (
	"fmt"
	"time"
)

type Config struct {
	Dir                 string
	RootPackage         string
	FactoryPackage      string
	GeneratedComment    string
	GenerateCopyMethods bool
	GenerateBuilders    bool
	GoFmt               bool
	Json                bool
	JsonCompact         bool
	Protobuf            bool
	GC                  bool
	NoGC                bool
	Debug               bool
}

func DefaultConfig(dir string) *Config {
	return &Config{
		Dir:                 dir,
		RootPackage:         "github.com/moontrade/dtc-go/v8",
		FactoryPackage:      "factory",
		GeneratedComment:    fmt.Sprintf("generated by github.com/moontrade/dtc-go/codegen/go at %s", time.Now().String()),
		GenerateCopyMethods: true,
		GoFmt:               true,
		Json:                true,
		JsonCompact:         false,
		Protobuf:            true,
		GC:                  true,
		NoGC:                true,
	}
}
