package rust

import (
	"fmt"
	"time"
)

type Config struct {
	Dir              string
	GeneratedComment string
	NonStandard      bool
}

func DefaultConfig(dir string) *Config {
	return &Config{
		Dir:              dir,
		GeneratedComment: fmt.Sprintf("generated by github.com/moontrade/dtc-go/codegen/go at %s", time.Now().String()),
		NonStandard:      false,
	}
}

func TinyGoConfig(dir string) *Config {
	return &Config{
		Dir:              dir,
		GeneratedComment: fmt.Sprintf("generated by github.com/moontrade/dtc-go/codegen/go at %s", time.Now().String()),
		NonStandard:      false,
	}
}
